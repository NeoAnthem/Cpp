// WAP to check whether the number is positive or negative
// input = 1, conditions = 2 (if else)
#include <iostream>
using namespace std;

int main()
{
    int num;
    cout << "Enter a number: ";
    cin >> num;

    if (num >= 0)
    {
        cout << "Number is positive\n";
    }
    else
    {

        cout << "Number is Negative\n";
    }
}